    private void BotaoConexaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoConexaoActionPerformed
        if (!Cliente.conectado()) {
            if (Cliente.conectarServidor()) {
                statusConexao.setForeground(new Color(0, 150, 0));
                statusConexao.setText("Conexão estabelecida com sucesso!");
                BotaoConexao.setText("Desconectar");
            } else {
                statusConexao.setForeground(new Color(200, 0, 0));
                statusConexao.setText("Não foi possível se conectar com o servidor.");
                BotaoConexao.setText("Conectar");
            }
        } else {
            Cliente.desconectarServidor();
            statusConexao.setForeground(Color.black);
            statusConexao.setText("Desconectado.");
            BotaoConexao.setText("Conectar");
        }
    }//GEN-LAST:event_BotaoConexaoActionPerformed

