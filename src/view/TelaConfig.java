    private void botaoContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoContinuarActionPerformed
        if(!Cliente.conectado()) {
            JOptionPane.showMessageDialog(null,"É necessário estar conectado ao servidor para continuar");
        } else {
        try {
                Cliente.enviarConfig(serial.getText(), region.getText(), protocolo.getText(), baudrate.getText(), potencia.getText());
            } catch (IOException ex) {
                //Logger.getLogger(TelaConfig.class.getName()).log(Level.SEVERE, null, ex);
            }

        TelaFinal tela = new TelaFinal();
        tela.setVisible(true);
        this.dispose();
        }
    }//GEN-LAST:event_botaoContinuarActionPerformed

